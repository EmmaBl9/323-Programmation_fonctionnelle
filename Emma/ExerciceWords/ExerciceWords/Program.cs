            List<string> words = new List<string> { "bonjour", "hello", "monde", "vert", "rouge", "bleu", "jaune" };

            // Afficher les éléments qui :
            // ne contiennent pas la lettre x
            // ont 4 caractères ou plus
            // ont autant de caractères que la moyenne du nombre de caractères par mot de la liste

            double avgCharCount = words.Average(word => word.Length);

            // Afficher les résultats
            // dans l’ordre inverse de celui naturellement calculé
            words.Where(word => !word.Contains("x") && word.Length >= 4 && word.Length == (int)avgCharCount).Reverse().ToList().ForEach(word => Console.Write(word + " "));
            Console.WriteLine();
            // triés a-z
            words.Where(word => !word.Contains("x") && word.Length >= 4 && word.Length == (int)avgCharCount).OrderBy(word => word).ToList().ForEach(word => Console.Write(word + " "));
            Console.WriteLine();

            // triés z-a
            words.Where(word => !word.Contains("x") && word.Length >= 4 && word.Length == (int)avgCharCount).OrderByDescending(word => word).ToList().ForEach(word => Console.Write(word + " "));
            Console.WriteLine("\n\n");

